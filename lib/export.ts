// Export utilities for generating PDFs and copying data to clipboard

export async function copyToClipboard(data: Record<string, any>[]): Promise<boolean> {
  if (data.length === 0) {
    alert('No data to copy')
    return false
  }

  const headers = Object.keys(data[0])
  const rows = [
    headers.join('\t'), // Use tabs for easy Excel paste
    ...data.map(row => 
      headers.map(header => {
        const value = row[header]
        if (value instanceof Date) {
          return value.toLocaleDateString()
        }
        if (value === null || value === undefined) {
          return ''
        }
        return String(value)
      }).join('\t')
    )
  ]
  
  const content = rows.join('\n')

  try {
    await navigator.clipboard.writeText(content)
    return true
  } catch (err) {
    console.error('Failed to copy:', err)
    alert('Failed to copy to clipboard')
    return false
  }
}

